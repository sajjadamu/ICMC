package com.chest.currency.viewBean;

public class BinColor {
private int id;
private String binType;
private String colorCode;
private String htmlColorCode;
private String binNum;

public String getBinNum() {
	return binNum;
}
public void setBinNum(String binNum) {
	this.binNum = binNum;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getBinType() {
	return binType;
}
public void setBinType(String binType) {
	this.binType = binType;
}
public String getColorCode() {
	return colorCode;
}
public void setColorCode(String colorCode) {
	this.colorCode = colorCode;
}
public String getHtmlColorCode() {
	return htmlColorCode;
}
public void setHtmlColorCode(String htmlColorCode) {
	this.htmlColorCode = htmlColorCode;
}

}
